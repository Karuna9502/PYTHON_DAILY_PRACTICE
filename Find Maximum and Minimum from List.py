'''Practice Problem: Identify the largest and smallest numerical values within a provided list.

Exercise Purpose: Finding extremes is vital for tasks like identifying the “best” price,
 the “highest” score, or detecting “outlier” data points in a dataset.'''

input = [45, 12, 89, 2, 67]
Maximum = max(input)
Minimum = min(input)

print("Maximum = ", Maximum)
print("Minimum = ", Minimum)
